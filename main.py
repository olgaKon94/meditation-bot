def главное_меню():
    кнопки = [
        ["🌅 Спокойное утро", "🌙 Тихий вечер"],
        ["😮‍💨 Подышать вместе", "🆘 Помоги успокоиться"],
        ["💬 Слова поддержки", "🎵 Музыка для души"]
    ]
    return ReplyKeyboardMarkup(кнопки, resize_keyboard=True)

async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "🧘 Привет! Я бот медитаций.\n\nВыберите практику, которая вам нужна прямо сейчас:",
        reply_markup=главное_меню()
    )

async def обработка(update: Update, context: ContextTypes.DEFAULT_TYPE):
    текст = update.message.text

    if текст == "💬 Слова поддержки":
        фраза = random.choice(СЛОВА_ПОДДЕРЖКИ)
        await update.message.reply_text(
            фраза,
            reply_markup=главное_меню()
        )
        return

    if текст == "🎵 Музыка для души":
        название, ссылка = random.choice(МУЗЫКА_ДЛЯ_ДУШИ)
        await update.message.reply_text(
            f"🎵 {название}\n\nНажмите чтобы слушать:\n{ссылка}\n\nВключите и начните медитацию 🧘",
            reply_markup=главное_меню()
        )
        return

    if текст in МЕДИТАЦИИ:
        await update.message.reply_text(
            МЕДИТАЦИИ[текст],
            parse_mode="Markdown",
            reply_markup=главное_меню()
        )
        return

    # если ничего не подошло
    await update.message.reply_text(
        "Выберите практику из меню 👇",
        reply_markup=главное_меню()
    )

async def тест(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await context.bot.send_message(
        chat_id="@2mantrywithlove",
        text="🧪 Тест автопостинга работает! 🎉"
    )
    await update.message.reply_text("Сообщение отправлено в канал!")

